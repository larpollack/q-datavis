let parseDate = d3.timeParse("%Y-%m-%d");
data.forEach(function(d) {
  d.values.forEach(function(d) {
    d.date = parseDate(d.date);
    d.value = Number(d.value);
  });
});

let xScale = d3
  .scaleTime()
  .domain(d3.extent(data[0].values, d => d.date))
  .range([0, width - margin]);

let yScale = d3
  .scaleLinear()
  .domain([0, d3.max(data[2].values, d => d.value)])
  .range([height - margin, 0]);

let yellow = d3.interpolateYlGn(0), // "rgb(255, 255, 229)"
  yellowGreen = d3.interpolateYlGn(0.5), // "rgb(120, 197, 120)"
  green = d3.interpolateYlGn(1); // "rgb(0, 69, 41)"

let color = d3.scaleOrdinal(d3.schemePRGn[6]);

let svg = d3
  .select("#linechart")
  .append("svg")
  .attr("width", width + margin + "px")
  .attr("height", height + margin + "px")
  .call(
    d3.zoom().on("zoom", function() {
      svg.attr("transform", d3.event.transform);
    })
  )
  .append("g")
  .attr("transform", `translate(${margin}, ${margin})`);

let line = d3
  .line()
  .x(d => xScale(d.date))
  .y(d => yScale(d.value));

let lines = svg.append("g").attr("class", "lines");

lines
  .selectAll(".line-group")
  .data(data)
  .enter()
  .append("g")
  .attr("class", "line-group")
  .on("mouseover", function(d, i) {
    svg
      .append("text")
      .attr("class", "title-text")
      .style("fill", color(i))
      .text(d.name)
      .attr("text-anchor", "middle")
      .attr("x", (width - margin) / 2)
      .attr("y", 350);
  })
  .on("mouseout", function(d) {
    svg.select(".title-text").remove();
  })
  .append("path")
  .attr("class", "line")
  .attr("d", d => line(d.values))
  .style("stroke", (d, i) => color(i))
  .style("opacity", lineOpacity)
  .on("mouseover", function(d) {
    d3.selectAll(".line").style("opacity", otherLinesHover);
    d3.selectAll(".circle").style("opacity", pointOpacityLineHover);
    d3.select(this)
      .style("opacity", lineOpacityHover)
      .style("stroke-width", lineStrokeHover)
      .style("cursor", "pointer");
  })
  .on("mouseout", function(d) {
    d3.selectAll(".line").style("opacity", lineOpacity);
    d3.selectAll(".circle").style("opacity", pointOpacity);
    d3.select(this)
      .style("stroke-width", lineStroke)
      .style("cursor", "none");
  });

